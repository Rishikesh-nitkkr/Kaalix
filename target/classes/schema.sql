-- KAALIX progression schema for MySQL/PostgreSQL style databases.
-- The app runs with Hibernate auto-update locally; use this as the production schema guide.

CREATE TABLE IF NOT EXISTS users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    user_key VARCHAR(255) NOT NULL UNIQUE,
    level INTEGER NOT NULL DEFAULT 1,
    txp INTEGER NOT NULL DEFAULT 0,
    next_level_xp INTEGER NOT NULL DEFAULT 120,
    rank VARCHAR(32) NOT NULL DEFAULT 'E',
    streak INTEGER NOT NULL DEFAULT 0,
    last_active_date DATE,
    initial_rank_locked BOOLEAN NOT NULL DEFAULT FALSE
);

CREATE TABLE IF NOT EXISTS daily_logs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL REFERENCES users(id),
    date DATE NOT NULL,
    study_hours DOUBLE PRECISION NOT NULL DEFAULT 0,
    dsa_count INTEGER NOT NULL DEFAULT 0,
    workout BOOLEAN NOT NULL DEFAULT FALSE,
    wake_on_time BOOLEAN NOT NULL DEFAULT FALSE,
    no_distractions BOOLEAN NOT NULL DEFAULT FALSE,
    skip_study BOOLEAN NOT NULL DEFAULT FALSE,
    late_wake BOOLEAN NOT NULL DEFAULT FALSE,
    overthinking_anger BOOLEAN NOT NULL DEFAULT FALSE,
    wasted_time DOUBLE PRECISION NOT NULL DEFAULT 0,
    tasks_completed INTEGER NOT NULL DEFAULT 0,
    total_tasks INTEGER NOT NULL DEFAULT 0,
    score INTEGER NOT NULL DEFAULT 0,
    txp_earned INTEGER NOT NULL DEFAULT 0
);

CREATE INDEX IF NOT EXISTS idx_daily_logs_user_date ON daily_logs(user_id, date);

CREATE TABLE IF NOT EXISTS songs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title VARCHAR(255) NOT NULL,
    artist VARCHAR(255),
    source VARCHAR(32) NOT NULL,
    source_url VARCHAR(1024),
    file_path VARCHAR(1024),
    stream_url VARCHAR(1024),
    duration VARCHAR(32),
    cover_image VARCHAR(1024),
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS playlists (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    type VARCHAR(64) NOT NULL DEFAULT 'SYSTEM',
    mood VARCHAR(255),
    created_by VARCHAR(255),
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS playlist_songs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    playlist_id BIGINT NOT NULL REFERENCES playlists(id),
    song_id BIGINT NOT NULL REFERENCES songs(id),
    added_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS user_music_preferences (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_key VARCHAR(255) NOT NULL UNIQUE,
    music_enabled BOOLEAN NOT NULL DEFAULT FALSE,
    volume DOUBLE PRECISION NOT NULL DEFAULT 0.35,
    repeat_enabled BOOLEAN NOT NULL DEFAULT FALSE,
    shuffle_enabled BOOLEAN NOT NULL DEFAULT FALSE
);

CREATE TABLE IF NOT EXISTS connected_music_sources (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_key VARCHAR(255) NOT NULL,
    source VARCHAR(32) NOT NULL,
    connected BOOLEAN NOT NULL DEFAULT FALSE,
    status_message VARCHAR(512),
    connected_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX IF NOT EXISTS idx_playlist_songs_playlist ON playlist_songs(playlist_id);
CREATE INDEX IF NOT EXISTS idx_connected_music_sources_user ON connected_music_sources(user_key);
